    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        // TODO add your handling code here:
        String tombol = bcari.getText();
        if (tombol.equals("Cari")){
        Object[] Baris = {"Kode", "Nama", "Alamat", "No Telepon"};
        tabmodel = new DefaultTableModel(null, Baris);
        tblpelanggan.setModel(tabmodel);
        sql = "Select * from pelanggan where kd_pelanggan like '%" + tcari.getText() + "%'" +
            "or nama_pelanggan like '%" + tcari.getText() + "%'";
        try {
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                kode = rs.getString("kd_pelanggan");
                nama = rs.getString("nama_pelanggan");
                alamat = rs.getString("alamat");
                telepon = rs.getString("no_telp");
                String[] data = {kode,nama,alamat,telepon};
                tabmodel.addRow(data);
                }
            }catch (Exception e) {
                }
            bcari.setText("Batal");
        }else{
            datatable();
            bcari.setText("Cari");
            tcari.setText("");
            bsimpan.setEnabled(true);
        }
    }//GEN-LAST:event_bcariActionPerformed

