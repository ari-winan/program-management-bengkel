    public void autoNumber(){
        try {
            sql = "SELECT * FROM pelanggan ORDER BY kd_pelanggan DESC";
            ps = conn.prepareCall(sql);
            rs = ps.executeQuery(sql);
            if (rs.next()) {
                kd = rs.getString("kd_pelanggan").substring(2);
                angka =  "" + (Integer.parseInt(kd) + 1);
                nol = "";

                if (angka.length()==1) {
                    nol = "000";
                } else if (angka.length()==2) {
                    nol = "00";
                } else if (angka.length()==3) {
                    nol = "0";
                } else if (angka.length()==4) {
                    nol = "";
                }
                tkode.setText("PL" + nol + angka);
            } else {
                tkode.setText("PL0001");
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    
